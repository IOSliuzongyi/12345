#import "UIScreen+CUAddition.h"

inline CGFloat CUScreenWidth()
{
    return [UIScreen mainScreen].bounds.size.width;
}

inline CGFloat CUScreenHeight()
{
    return [UIScreen mainScreen].bounds.size.height;
}

@implementation UIScreen (CUAddition)

@end
